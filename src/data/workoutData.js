export const getTodayWorkoutType = () => {
  const today = new Date().getDay(); // 0 = Sun
  // 0 Sun rest, 1 Mon push, 2 Tue rest, 3 Wed pull, 4 Thu rest,
  // 5 Fri leg, 6 Sat pull
  const map = ['rest', 'push', 'rest', 'pull', 'rest', 'leg', 'pull'];
  return map[today];
};
